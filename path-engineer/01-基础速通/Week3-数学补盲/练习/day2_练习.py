# 题 3（核心）：手撸 Attention 的 Q @ K^T
# =============================================================================
# 这是今天最重要的一题 —— 你将计算一个注意力矩阵

def attention_score_demo():
    np.random.seed(42)

    # 假设：序列长度 seq_len = 4，每个 token 的 Query/Key 维度 d_k = 3
    seq_len = 4
    d_k = 3

    # 假装我们已经有 Q 和 K（实际是通过线性变换得到的）
    Q = np.random.randn(seq_len, d_k)
    K = np.random.randn(seq_len, d_k)

    print(f"Q.shape = {Q.shape}, K.shape = {K.shape}")

    # TODO: 算 Q @ K.T，得到相似度矩阵
    # scores = ???
    scores = Q @ K.T

    print(f"\nscores.shape = {scores.shape}   # 应该是 (4, 4)")
    print(f"scores = \n{scores}\n")

    # 手动验证：scores[0, 2] 应该等于 Q[0] 和 K[2] 的点积
    manual = np.dot(Q[0], K[2])
    print(f"验证 scores[0, 2] = {scores[0, 2]:.4f}")
    print(f"手算 Q[0] · K[2] = {manual:.4f}  (应该相等)")

    print()
    print("💡 解读：scores[i, j] 表示第 i 个 token 对第 j 个 token 的'关注度'")
    print("   每一行（4 个数）就是该 token 对全部 token 的注意力原始分数")
    print("   明天 Day 3 会用 softmax 把这些分数变成概率（0-1）")


