# 题 5（简单神经网络）：模拟 3 层全连接
# =============================================================================
# 输入一个 100×64 的 batch，经过 3 层全连接，每层把维度从 64→128→64→10

def simple_nn_forward():
    np.random.seed(42)

    # 输入：batch=100, 每条 64 维
    X = np.random.randn(100, 64)

    # 3 个权重矩阵
    W1 = np.random.randn(64, 128)
    W2 = np.random.randn(128, 64)
    W3 = np.random.randn(64, 10)

    # TODO: 分别算每一层的输出 shape
    h1 = X @ W1         # (100, 128)
    h2 = h1 @ W2        # (100, 64)
    out = h2 @ W3       # (100, 10)

    print(f"输入 X:  {X.shape}")
    print(f"第 1 层输出 h1: {h1.shape}")
    print(f"第 2 层输出 h2: {h2.shape}")
    print(f"第 3 层输出 out: {out.shape}")
    print()
    print("💡 这就是神经网络'前向传播'的最小版（没加激活函数）")


